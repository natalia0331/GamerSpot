package com.example.gamerspotv2.models;

import java.util.List;

public class EventRelease {
    private String id;
    private String type; // "event" or "release"
    private String title;
    private String description;
    private String date; // Use LocalDateTime for date-time handling
    private String time;
    private String location;
    private List<String> platforms;
    private List<String> purchaseLinks;
    private List<String> media;
    private String developerId;

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public List<String> getPlatforms() {
        return platforms;
    }

    public void setPlatforms(List<String> platforms) {
        this.platforms = platforms;
    }

    public List<String> getPurchaseLinks() {
        return purchaseLinks;
    }

    public void setPurchaseLinks(List<String> purchaseLinks) {
        this.purchaseLinks = purchaseLinks;
    }

    public List<String> getMedia() {
        return media;
    }

    public void setMedia(List<String> media) {
        this.media = media;
    }

    public String getDeveloperId() {
        return developerId;
    }

    public void setDeveloperId(String developerId) {
        this.developerId = developerId;
    }

}


